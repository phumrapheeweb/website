function changeLanguage(lang) {
    if (lang === currentLanguage) return;
    
    currentLanguage = lang;
    
    // Update current language display
    const currentLangElement = document.getElementById('currentLang');
    currentLangElement.textContent = lang === 'en' ? 'üá∫üá∏ EN' : 'üáπüá≠ TH';
    
    // Update language options active state
    document.querySelectorAll('.language-option').forEach(option => {
        option.classList.remove('active');
        if ((lang === 'en' && option.textContent.includes('English')) ||
            (lang === 'th' && option.textContent.includes('‡πÑ‡∏ó‡∏¢'))) {
            option.classList.add('active');
        }
    });
    
    // Update page language
    document.documentElement.lang = lang;
    
    // Add fade effect during translation
    document.body.classList.add('fade-transition');
    
    setTimeout(() => {
        // Update all translatable elements
        document.querySelectorAll('[data-key]').forEach(element => {
            const key = element.getAttribute('data-key');
            if (translations[lang] && translations[lang][key]) {
                element.innerHTML = translations[lang][key];
            }
        });
        
        // Remove fade effect
        document.body.classList.remove('fade-transition');
        document.body.classList.add('active');
    }, 150);
    
    // Close dropdown
    document.getElementById('languageDropdown').classList.remove('active');
}

